// the model
public class Player {
	private String name;
	private int shirtNumber;
	
	public String getName() {
		return name;
	}
	
	public int getShirtNumber() {
		return shirtNumber;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setShirtNumber(int shirtNumber) {
		this.shirtNumber = shirtNumber;
	}
}
